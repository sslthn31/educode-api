const mongoose = require('mongoose');
const schema = mongoose.Schema;

export const courseJsModel = new schema({
  courseId: {
    type: String,
    required: true,
  },
  title: {
    type: String,
    required: true,
  },
  link: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
});
